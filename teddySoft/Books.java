package teddySoft;

public class Books {
	private
		String title;
		String author;
		String edition;
		int isbn;
		String pubdate;
	
	public String getTitle(){
		return title;
	}
	
	public String getAuthor(){
		return author;
	}
	
	public String getEdition(){
		return edition;
	}
	
	public int getIsbn(){
		return isbn;
	}
	
	public String getPubdate(){
		return pubdate;
	}
}
